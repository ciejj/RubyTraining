# logical operators - negation - !

# negation operator flips the boolean value of the expression after it
puts "!true: #{!true}"
puts "!false: #{!false}"
puts "!'a string': #{!'a string'}"
puts "!nil: #{!nil}"

# double negation -  maps the expression to its boolean value
puts "!!true: #{!!true}"
puts "!!false: #{!!false}"
puts "!!'a string': #{!!'a string'}"
puts "!!nil: #{!!nil}"
