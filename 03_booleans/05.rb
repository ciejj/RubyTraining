# logical operators - !

value1 = true 
puts "  value1: #{value1}"
puts " !value1: #{!value1}"
puts "!!value1: #{!!value1}"

value2 = 'a non empty string'
puts "  value2: #{value2}"
puts " !value2: #{!value2}"
puts "!!value2: #{!!value2}"

value3 = nil
puts " value3: #{value3}"
puts "!value3: #{!value3}"
puts "!!value3: #{!!value3}"
